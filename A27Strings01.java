/*
 * String : Collection of 'Character' enclosed within " " (double quote)
 * Two types of String : Mutable and Immutable
 * 
 */

public class A27Strings01 {
    public static void main(String[] args) {

       /*
        * String : Collection of Character enclosed within " " (double quote) 
                "abcd"
                "Anuj"
                "@#%%&%^"
                "1226%$^&"
                "6258721"
        */ 

        /*
         * To Store string type data we have to use an inbuilt 
         * class there is no any primitive data type for string .
         * 
         * Here we have used 'String' class which is under 'lang'package 
         * lang package is a default package and defaultly imported.
         * 
         */
        String name = "Anuj";
        System.out.println(name);
        
        
    }
}
